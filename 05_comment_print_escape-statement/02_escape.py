'''this \n is a escape statement which is used to escape.
for example'''

print("Hello, I am Kuriya \tI am suryakant")
# Output:  Hello, I am Kuriya      I am suryakant

print("\\n This is a Newline Escape Statement")
# Output: \n This is a Newline Escape Statement
#\\n is used to print \n

print("\" This is used to print Double Quote")
# Output: " This is used to print Double Quote
print("\"Hello\" \"World\"")
# Output: "Hello" "World"