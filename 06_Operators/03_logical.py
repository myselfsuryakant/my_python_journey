'''logical operators and, or, not
used in boolean operators
and: dono situations True honi chahiye
or: koi bhi ek True honi chahiye
not: and ka ulta
'''
print('True and True: ',True and True)
print('True and False: ',True and False)
print('False and True: ',False and True)
print('False and False: ',False and False, end="\n\n")

print('True or True: ',True or True)
print('True or False: ',True or False)
print('False or True: ',False or True)
print('False or False: ',False or False, end="\n\n")

print('not(True): ',not(True))
print('not(False): ',not(False), end="\n\n")
